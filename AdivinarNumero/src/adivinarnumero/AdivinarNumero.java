    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        int num1;
        // TODO add your handling code here:
        num1=Integer.parseInt(txtIngresoNumero.getText());
        if(num==num1) {
            lblInformacion.setText("Correcto!");
            this.getContentPane().setBackground(Color.green);
            txtIngresoNumero.enable(false);
        }
         else if(num1>num && num1>(num+35)){
             lblInformacion.setText("DemasiadoAlto");
             this.getContentPane().setBackground(Color.blue);
             txtIngresoNumero.setText("");
    }//GEN-LAST:event_btnIngresarActionPerformed
    else if(num1>num && num1<(num+35)){
             lblInformacion.setText("DemasiadoAlto");
             this.getContentPane().setBackground(Color.red);
             txtIngresoNumero.setText("");
    }    
         else if(num1<num  && num1<num-35){
             lblInformacion.setText("DemasiadoBajo");
             this.getContentPane().setBackground(Color.blue);
             txtIngresoNumero.setText("");
    }         
        else if(num1<num  && num1>num-35){
             lblInformacion.setText("DemasiadoBajo");
             this.getContentPane().setBackground(Color.red);
             txtIngresoNumero.setText("");
    }         
        }
